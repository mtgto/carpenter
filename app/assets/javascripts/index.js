$(function(){
    $('#change_password_cancel').click(
        function(){
            $('#modal_change_password').modal('hide');
        }
    );
    $('a.change_password').click(
        function(){
            $('#modal_change_password').modal('show');
        }
    );
    $('#change_password_submit').click(
        function(){
            $('#form_change_password').submit();
        }
    );
});
